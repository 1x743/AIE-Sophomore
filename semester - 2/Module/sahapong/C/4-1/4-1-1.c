#include <stdio.h>

int main() {
    // ขั้นตอนที่ 1: ประกาศตัวแปรชนิดจำนวนเต็มชื่อว่า Dido แบบอาร์เรย์ที่มีขนาดเท่ากับ 5
    int Dido[5];

    // ขั้นตอนที่ 2: ประกาศตัวแปรชนิดเลขทศนิยมชื่อว่า Fifa แบบอาร์เรย์ที่มีขนาดเท่ากับ 3
    // และให้ค่าเริ่มต้นกับตัวแปรดังนี้ 4.5, 10.5 และ 20.5
    float Fifa[3] = {4.5, 10.5, 20.5};

    // ขั้นตอนที่ 3: รับค่าจากคีย์บอร์ดไปเก็บไว้ที่สมาชิกของตัวแปร Dido จนครบทุกตัว โดยใช้ for loop
    printf("\nกรุณาป้อนค่าให้กับสมาชิกของอาร์เรย์ Dido:\n");
    for (int i = 0; i < 5; i++) {
        printf("Dido[%d] = ", i);
        scanf("%d", &Dido[i]);
    }

    // ขั้นตอนที่ 4: แสดงค่าตัวแปร Fifa ทั้งหมด ออกทางจอภาพ โดยใช้ for loop
    printf("\nค่าของตัวแปร Fifa:\n");
    for (int i = 0; i < 3; i++) {
        printf("Fifa[%d] = %.2f\n", i, Fifa[i]);
    }

    // ขั้นตอนที่ 5: แสดงค่าตัวแปร Dido ทั้งหมด ออกทางจอภาพ โดยใช้ for loop
    printf("\nค่าของตัวแปร Dido:\n");
    for (int i = 0; i < 5; i++) {
        printf("Dido[%d] = %d\n", i, Dido[i]);
    }

    // ขั้นตอนที่ 6: รับค่าใหม่ เก็บไว้ที่ตัวแปร Fifa จนครบทุกตัว โดยใช้ for loop
    printf("\nกรุณาป้อนค่าใหม่ให้กับสมาชิกของอาร์เรย์ Fifa:\n");
    for (int i = 0; i < 3; i++) {
        printf("Fifa[%d] = ", i);
        scanf("%f", &Fifa[i]);
    }

    // ขั้นตอนที่ 7: แสดงค่าตัวแปร Fifa ออกทางจอภาพ โดยใช้ for loop
    printf("\nค่าของตัวแปร Fifa หลังการอัปเดต:\n");
    for (int i = 0; i < 3; i++) {
        printf("Fifa[%d] = %.2f\n", i, Fifa[i]);
    }

    // ขั้นตอนที่ 8: แสดงค่าตัวแปร Dido ออกทางจอภาพ โดยใช้ for loop
    printf("\nค่าของตัวแปร Dido:\n");
    for (int i = 0; i < 5; i++) {
        printf("Dido[%d] = %d\n", i, Dido[i]);
    }

    return 0;
}
